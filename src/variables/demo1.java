package variables;

public class demo1 {
public static void main(String[] args) {
	//variable declaration/allocating reserving memory
	
	String studentName;
	int studentRollno;            //datatype variable name
	float studentPercentage;
	char studentGrade;
	
	//variable initialization /assigning or inserting value
	studentName="Neil";
	studentRollno=12;
	studentPercentage=85.6f;       //variablename=variablevalue
	
	studentGrade='A';  
	
	//variable usage
	System.out.println(studentName);
	System.out.println(studentRollno);      //info reuse
	System.out.println(studentPercentage);
	System.out.println(studentGrade);
	
}
}
