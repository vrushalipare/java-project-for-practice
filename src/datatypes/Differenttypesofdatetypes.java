package datatypes;

public class Differenttypesofdatetypes {
public static void main(String[] args) {
	   
	String name="Mohan";
	int rNo=101;         //rNo -rollnumber
	float per=86.7f;    //per-percentage
	char g='A';         //g -grade 
	
	System.out.println("Student name="+ name);
	System.out.println("Student rollNumber="+rNo);
	System.out.println("Student percentage="+per);
	System.out.println("Student grade="+g);
	
}
}
