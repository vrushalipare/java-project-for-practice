package datatypes;

public class Datatypesadditionexamples {
public static void main(String[] args) {
	String name="Kavita";
	int rNo=100;
	float per=78.4f;
	char g='B';
	
	System.out.println("Player name="+name);
	System.out.println("Player rollNo="+rNo);
	System.out.println("Player percentage="+per);
	System.out.println("Player grade="+g);
	
	
	
	String n="John";
	int srno=10;
	float p=67.7f;
	char gr='C';
	
	System.out.println("Student name="+n);
	System.out.println("Student serialnumber="+srno);
	System.out.println("Student precentage="+p);
	System.out.println("Student grade="+gr);
}
}
